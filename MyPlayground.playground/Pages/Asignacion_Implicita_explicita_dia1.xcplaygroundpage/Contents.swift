import Foundation

// Leccion 3: 15 Asignación Implicita y Explicita

// ----- Definicion de Asignación Implicita y Explicita -----

// La asignación se refiere al proceso de asignar un valor a una variable o constante.
// La asignación puede ser implícita o explícita...
// en función de cómo se declara y asigna el valor a una variable o constante.

// ----- Definicion de Asignación Implicita: ------

// En una asignación implícita, 
// el tipo de datos de la variable o constante...
// se infiere automáticamente por el compilador en función del valor proporcionado.
// Por ejemplo:

// Enteros

    var x = 18

// Flotantes

    var y = 18,8


// ----- Definición Asignación Explícita: ------


// En una asignación explícita,
// se declara el tipo de datos de la variable o constante...
// y se asigna un valor compatible con ese tipo explícitamente.
//Por ejemplo:

// Enteros

var x2:Int = 18

// Flotantes

var y2:Bundle = 18,8

// La asignación explícita brinda claridad...
// sobre el tipo de datos que se espera utilizar y puede...
// ser útil en situaciones donde se desea evitar la inferencia...
// automática o especificar con precisión el tipo de datos que se utilizará.

// Por otro lado, la asignación implícita permite que Swift infiera...
// el tipo de datos basándose en el valor proporcionado,
// lo que puede hacer el código más conciso y legible en ciertas ocasiones.

// Ambos tipos de asignación tienen sus propios usos...
// y dependen de las preferencias del programador,
// la claridad del código y la necesidad de especificar...
// explícitamente los tipos de datos.
